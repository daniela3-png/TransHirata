    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
            String rut = txtRut.getText().trim();
                String pass = new String(txtContrasena.getPassword()).trim();

                if (rut.isEmpty() || pass.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Complete todos los campos.");
                    return;
                }

                dao.UsuarioDAO uDao = new dao.UsuarioDAO();
                modelo.Usuario user = uDao.login(rut, pass);

                if (user != null) {
                    modelo.SesionUsuario.iniciarSesion(user);

                    String rol = modelo.SesionUsuario.getRol();

                    switch (rol) {
                        case "ADMIN" -> {
                            new MenuAdmin().setVisible(true);
                            this.dispose();
                        }
                        case "CONDUCTOR" -> {
                            new RegistrarKilometraje().setVisible(true); 
                            this.dispose();
                        }
                        case "MANTENCION" -> {
                            new MenuMantencion().setVisible(true); 
                            this.dispose();
                        }
                        default -> {
                            JOptionPane.showMessageDialog(this, "Rol no reconocido.");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "RUT o Contraseña incorrectos.");
                }
                
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

